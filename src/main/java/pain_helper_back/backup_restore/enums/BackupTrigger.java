package pain_helper_back.backup_restore.enums;


/*
 * Триггеры запуска операций бэкапа.
 *
 * НАЗНАЧЕНИЕ: Отслеживание причины создания бэкапа для аудита
 */
public enum BackupTrigger {
    /*
     * Автоматический бэкап по расписанию.
     *
     * РАСПИСАНИЕ:
     * - H2: ежедневно в 02:00
     * - MongoDB: ежедневно в 03:00
     * - Полный: еженедельно в воскресенье в 01:00
     *
     * ИНИЦИАТОР: BackupScheduler
     */
    SCHEDULED,

    /*
     * Ручной бэкап через REST API.
     *
     * ИНИЦИАТОР: Администратор через POST /api/backup/create
     * ПОЛЕ initiatedBy: содержит ID пользователя
     */
    MANUAL,

    /*
     * Бэкап перед критической операцией.
     *
     * ПРИМЕРЫ:
     * - Перед обновлением системы
     * - Перед массовым импортом данных
     * - Перед восстановлением из другого бэкапа
     *
     * ИНИЦИАТОР: Система автоматически
     */
    PRE_OPERATION
}
