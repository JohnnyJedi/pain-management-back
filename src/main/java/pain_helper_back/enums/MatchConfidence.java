package pain_helper_back.enums;

/**
 * Уровень уверенности при сопоставлении пациента из внешней системы с существующим пациентом.
 * Используется в Patient Master Index для определения качества совпадения.
 */
public enum MatchConfidence {

    //Точное совпадение (100%): совпадают все ключевые поля (страховой полис + ФИО + дата рождения).
    EXACT,

    //Высокая уверенность (>90%): совпадают ФИО, дата рождения, пол.
    HIGH,

    //Средняя уверенность (70-90%): совпадают фонетически похожие имена + дата рождения.
    MEDIUM,

    //Низкая уверенность (<70%): частичные совпадения, требуется ручная проверка.
    LOW,

    //Совпадений не найдено - новый пациент.
    NO_MATCH
}
